import Foundation
import UIKit

extension UIFont {
    static func exFontLatoSemiBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Semibold", size: size)!
    }
    
    static func exFontLatoBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Bold", size: size)!
    }
    
    static func exFontLatoRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Regular", size: size)!
    }
}
